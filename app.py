model_cache = {}

def get_model_and_meta(blade_id):
    """Retrieves AI model and metadata. Uses compile=False to fix Render version errors."""
    blade_id = str(blade_id)
    if blade_id in model_cache:
        return model_cache[blade_id]

    try:
        if blade_id in ["0", "1"]:
            # Original Master Model
            model_path = os.path.join(MODELS_DIR, "gyro_master_model.keras")
            meta_path = os.path.join(MODELS_DIR, "blade_meta.pkl")
            meta_data = joblib.load(meta_path)
            meta = meta_data['B1'] if blade_id == "0" else meta_data['B2']
        else:
            # Custom Blade Isolated Path
            path = os.path.join(CUSTOM_DIR, blade_id)
            model_path = os.path.join(path, "model.keras")
            meta_path = os.path.join(path, "meta.pkl")
            meta = list(joblib.load(meta_path).values())[0]

        # FIX: compile=False ignores quantization_config errors in Render's TF version
        model = tf.keras.models.load_model(model_path, compile=False)
        model_cache[blade_id] = (model, meta)
        return model, meta
    except Exception as e:
        print(f"❌ Critical Model Load Error for {blade_id}: {e}")
        return None, None

