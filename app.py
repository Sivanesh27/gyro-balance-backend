@app.route('/add-blade', methods=['POST'])
def add_blade():
    """Saves STL and extracts DNA for a new blade profile"""
    if 'stl' not in request.files: return jsonify({"error": "No STL file"}), 400
    
    file = request.files['stl']
    blade_id = str(uuid.uuid4())[:8]
    folder = os.path.join(CUSTOM_DIR, blade_id)
    os.makedirs(folder, exist_ok=True)
    
    stl_path = os.path.join(folder, "blade.stl")
    file.save(stl_path)
    
    # Physics Extraction
    mesh = trimesh.load(stl_path)
    density = float(request.form.get('density', 1.25))
    scale = 0.1
    ideal_cg = float(mesh.centroid[0]) * scale
    ideal_mass = (float(mesh.volume) * (scale**3) * density) / 1000
    
    meta = {blade_id: [ideal_cg, ideal_mass]}
    joblib.dump(meta, os.path.join(folder, "meta.pkl"))

    # Copy master model as base (Isolated Training would happen here)
    master_path = os.path.join(MODELS_DIR, "gyro_master_model.keras")
    if os.path.exists(master_path):
        target_model_path = os.path.join(folder, "model.keras")
        models.load_model(master_path, compile=False).save(target_model_path)

    return jsonify({"status": "registered", "blade_id": blade_id, "cg": ideal_cg})

@app.route('/download-report', methods=['POST'])
def download_report():
    """Generates the professional PDF with all measured data"""
    data = request.json
    pdf = ProfessionalReport()
    pdf.add_page()
    
    pdf.bold_heading("1. Blade Profile Information")
    pdf.data_line("Timestamp", datetime.now().strftime("%Y-%m-%d %H:%M:%S"))
    pdf.data_line("Design Target CG", f"{data.get('target_cg', 0)} cm")
    pdf.data_line("Design Target Mass", f"{data.get('target_mass', 0)*1000:.1f} g")
    
    pdf.ln(5)
    pdf.bold_heading("2. Raw Load Analytics (3 Points)")
    pdf.data_line("Root Zone Load (S1)", f"{data.get('w_root', 0)} g")
    pdf.data_line("Mid Span Load (S2)", f"{data.get('w_mid', 0)} g")
    pdf.data_line("Tip Zone Load (S3)", f"{data.get('w_tip', 0)} g")
    pdf.data_line("Total Measured Mass", f"{data.get('w_root',0)+data.get('w_mid',0)+data.get('w_tip',0):.1f} g")

    pdf.ln(5)
    pdf.bold_heading("3. AI Neural Diagnostic Result")
    pdf.data_line("Measured Center of Gravity", f"{data.get('cg', 0)} cm")
    pdf.data_line("Axial Drift / Deviation", f"{data.get('deviation', 0)} cm")
    pdf.data_line("Classification", data.get('status', 'Unknown'))
    pdf.set_font('Arial', 'I', 10)
    pdf.multi_cell(0, 8, f"AI INTERPRETATION: {data.get('status_desc', '')} {data.get('diagnosis_details', '')}")
    
    pdf.ln(5)
    pdf.bold_heading("4. Engineering Mitigation Plan")
    correction = data.get('correction', {})
    if correction.get('action') != "NONE":
        pdf.set_text_color(200, 0, 0) # Red
        pdf.data_line("Required Action", correction.get('action'))
        pdf.data_line("Counterweight Mass", f"{correction.get('mass')} g")
        pdf.data_line("Placement Coordinate", f"{correction.get('location')} cm from Root")
    else:
        pdf.set_text_color(0, 150, 0) # Green
        pdf.cell(0, 10, "VALIDATED: No structural mitigation required.", 0, 1)

    pdf_path = os.path.join(BASE_DIR, "report.pdf")
    pdf.output(pdf_path)
    return send_file(pdf_path, as_attachment=True)

